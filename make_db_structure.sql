CREATE TABLE police_requests (

    crime_id INTEGER,
    crime_type VARCHAR,
    report_date NUMERIC,
    call_date NUMERIC,
    offense_date NUMERIC,
    call_time NUMERIC,
    call_date_time NUMERIC,
    disposition VARCHAR,
    address VARCHAR,
    city VARCHAR,
    state VARCHAR,
    agency_id INTEGER,
    address_type VARCHAR,
    common_location VARCHAR
);

